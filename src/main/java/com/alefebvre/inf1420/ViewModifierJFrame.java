    public ViewModifierJFrame(ControllerFilsRSS fichierControlleur, ModelListeEvenementsTable listeEvenementsTableModel, int row) {
        this.fichierControlleur = fichierControlleur;
        initComponents();
        ModelEvenement evenementCourant = this.fichierControlleur.fils.listeEvenements.get(row);
        evenementControlleur = new ControllerEvenement();
        evenementControlleur.setTitre(evenementCourant.getTitre());
        evenementControlleur.setDescription(evenementCourant.getDescription());
        evenementControlleur.setLien(evenementCourant.getLien());
        evenementControlleur.setDate(evenementCourant.getDate());

        this.jTextField1.setText(evenementControlleur.evenement.getTitre());
        this.jTextField2.setText(evenementControlleur.evenement.getDescription());
        this.jTextField3.setText(evenementControlleur.evenement.getLien());
        this.jFormattedTextField1.setText(evenementControlleur.evenement.getDate());
        this.listeEvenementsTableModel = listeEvenementsTableModel;
        this.row = row;
    }

